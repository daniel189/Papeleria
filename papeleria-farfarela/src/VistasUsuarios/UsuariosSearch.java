    /**
     * 
     * @param evt evento de clickeo de mouse que setea el label de permiso segun
     * el nivel de accesso que se confiera en la columna.
     * 
     */
    private void tbCuentasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCuentasMouseClicked
        int fila = tbCuentas.getSelectedRow();
        if(fila >= 0){
            lbCuenta.setText(tbCuentas.getValueAt(fila, 0).toString());
            //lbConstraseña.setText(tbCuentas.getValueAt(fila, 2).toString());
            String n;
            n = tbCuentas.getValueAt(fila, 2).toString();
            switch(n){
                case "1":
                    lbNivel.setText("Administrador");
                    break;
                case "2":
                    lbNivel.setText("Vendedor");
                    break;
                default:
                    lbNivel.setText("Bodegero");
                    break;
            }
            lbEmpleado.setText(tbCuentas.getValueAt(fila, 1).toString());
        }
        else{
            JOptionPane.showMessageDialog(null, "Fila no selecionada.", "Sin seleccion.", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_tbCuentasMouseClicked

