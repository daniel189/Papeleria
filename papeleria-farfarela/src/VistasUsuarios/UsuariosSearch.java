    /**
     * Funcion encargada de llear la tabla de contenidos con el uso de la 
     * variable de condicion o de nivel
     * 
     * @param condicion es el nombre de la cuenta que se busca
     * @param aux  es la variable que refiere al nivel de accesso del usuario
     */
    private void llenarCuentas(String condicion, int aux){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Cuenta");
        modelo.addColumn("Cod Empleado");
        modelo.addColumn("Nivel");
        modelo.addColumn("Habilitado");
        tbCuentas.setModel(modelo);
        String sql;
        if(aux == 1){ //cuenta
            sql = "SELECT * FROM cuenta WHERE cue_cuenta LIKE '%"+condicion+"%'";
        }
        else{ //nivel
            sql = "SELECT * FROM cuenta WHERE cue_nivel LIKE '%"+condicion+"%'";
        }
        String datos[] = new String[4];
        try {
            Statement st = cone.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = Integer.toString(rs.getInt(2));
                //datos[2] = rs.getString(3);
                datos[2] = Integer.toString(rs.getInt(4));
                if(rs.getString(5).equals("1")){
                    datos[3] = "Si";
                }else{
                    datos[3] = "No";
                }
                modelo.addRow(datos);
            }
            tbCuentas.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(UsuariosEdit.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Error al traer las cuentas", "Error DB", JOptionPane.ERROR_MESSAGE);
        }
    }
